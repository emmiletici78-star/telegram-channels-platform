// FuncÈ›ia pentru obÈ›inerea limbii curente
function getCurrentLanguage() {
  return localStorage.getItem('language') || 'ro';
}

// FuncÈ›ia pentru setarea limbii
function setLanguage(lang) {
  localStorage.setItem('language', lang);
  applyTranslations(lang);
}

// FuncÈ›ia pentru aplicarea traducerilor
function applyTranslations(lang = getCurrentLanguage()) {
  const t = translations[lang] || translations['ro'];
  
  // AplicÄƒ traducerile pentru toate elementele cu data-i18n
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    if (t[key]) {
      // Pentru input-uri, seteazÄƒ placeholder-ul
      if (element.tagName === 'INPUT' && element.hasAttribute('placeholder')) {
        element.placeholder = t[key];
      } else {
        element.textContent = t[key];
      }
    }
  });
  
  // SeteazÄƒ limba Ã®n HTML
  document.documentElement.lang = lang === 'ro' ? 'ro' : lang;
  
  // ActualizeazÄƒ selectorul de limbÄƒ
  const languageSelector = document.getElementById('language-selector');
  if (languageSelector) {
    languageSelector.value = lang;
  }
}

